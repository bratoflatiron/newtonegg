!*************************************************************
! Sum chebyshev polynomial weighted by coefficients 
!*************************************************************
subroutine sumcoeff(chebT,coeff,S)
implicit none 
real(kind=8), intent(in) :: chebT(8), coeff(8)
real(kind=8), intent(out):: S
real(kind=8) :: v(8) 

v = chebT*coeff; 
S = sum(v); 

end subroutine sumcoeff 